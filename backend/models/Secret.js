const mongoose = require('mongoose');

const SecretSchema = new mongoose.Schema({
  text: {
    type: String,
    required: true
  },
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User'
  },
  date: {
    type: Date,
    default: Date.now
  }
});

const Secret = mongoose.model('Secret', SecretSchema);

module.exports = Secret;
